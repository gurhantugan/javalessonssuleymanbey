package day11loops;

public class DoWhileLoop01 {

	public static void main(String[] args) {
		// While-loop body may not be executed in some conditions
		// But do-while body executed at least once.
		
		int i = 1 ;
		
		while (i<1) {
			System.out.println("Hello!");
			i--;
			}   		// we get nothing
		
		
		int k = 1;
		
		do {
			System.out.println("Hello!");
			k++;
		} while (k<1);				// we get hello
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
